# –î–æ–±–∞–≤–ª–µ–Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –ø–æ–¥–∫–æ–º–∞–Ω–¥, –∞ —Ç–∞–∫–∂–µ "–∑–∞–≥–ª—É—à–∫–∞" –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
# –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ (—è–≤–ª—è–µ—Ç—Å—è —Ö–æ—Ä–æ—à–µ–π –ø—Ä–∞–∫—Ç–∏–∫–æ–π –¥–ª—è –ø–æ–¥–æ–±–Ω—ã—Ö –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤)
class DevStrategy(CommandStrategy):
    def __init__(self):
        # –ó–∞–≥—Ä—É–∂–∞–µ–º ID –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤ –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è (—É–∫–∞–∑–∞–Ω–æ –≤ .env —Ñ–∞–π–ª–µ)
        admin_ids_str = os.getenv("ADMIN_IDS", "")
        self.admin_ids = [int(admin_id) for admin_id in admin_ids_str.split(',') if admin_id.isdigit()]

        # –°–ª–æ–≤–∞—Ä—å –¥–ª—è —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω–∏—è –ø–æ–¥–∫–æ–º–∞–Ω–¥ —Å –º–µ—Ç–æ–¥–∞–º–∏-–æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞–º–∏
        self.dev_commands = {
            "get_ids": self._get_ids,
            "help": self._show_help,    # <--- –í–æ—Ç –∑–¥–µ—Å—å –º—ã —Å–≤—è–∑—ã–≤–∞–µ–º –ø–æ–¥–∫–æ–º–∞–Ω–¥—É "help"
        }

    def handle(self, text, chat_id, user_id):
        # –ø—Ä–æ–≤–µ—Ä–∫–∞ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ –≤–∫–ª—é—á–µ–Ω–∞ –∏ —Ä–∞–±–æ—Ç–∞–µ—Ç —Å .env
        if user_id not in self.admin_ids:
            return "‚õî –£ –≤–∞—Å –Ω–µ—Ç –¥–æ—Å—Ç—É–ø–∞ –∫ —ç—Ç–æ–π –∫–æ–º–∞–Ω–¥–µ."

        # –†–∞–∑–±–∏–≤–∞–µ–º —Ç–µ–∫—Å—Ç –∫–æ–º–∞–Ω–¥—ã –Ω–∞ —á–∞—Å—Ç–∏, —á—Ç–æ–±—ã –æ—Ç–¥–µ–ª–∏—Ç—å –∫–æ–º–∞–Ω–¥—É –æ—Ç –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤
        parts = text.split()
#        sub_command = parts[1] if len(parts) > 1 else None
        # parts[0] —ç—Ç–æ /dev, parts[1] –º–æ–∂–µ—Ç –±—ã—Ç—å –ø–æ–¥–∫–æ–º–∞–Ω–¥–æ–π
        sub_command = parts[1] if len(parts) > 1 else "get_ids"
        args = parts[2:] if len(parts) > 2 else []

        # –í—ã–±–∏—Ä–∞–µ–º –Ω—É–∂–Ω—ã–π –º–µ—Ç–æ–¥ –∏–∑ —Å–ª–æ–≤–∞—Ä—è –∏–ª–∏ –º–µ—Ç–æ–¥ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
        handler = self.dev_commands.get(sub_command, self._unknown_command)

        # # –í—ã–∑—ã–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫, –ø–µ—Ä–µ–¥–∞–≤–∞—è –µ–º—É –∞—Ä–≥—É–º–µ–Ω—Ç—ã
        # return handler(chat_id=chat_id, user_id=user_id, args=args)

        try:
            # –ë–µ–∑–æ–ø–∞—Å–Ω–æ –≤—ã–∑—ã–≤–∞–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫
            return handler(chat_id=chat_id, user_id=user_id, args=args)
        except Exception as e:
            # –í —Å–ª—É—á–∞–µ –æ—à–∏–±–∫–∏ –≤ –ø–æ–¥–∫–æ–º–∞–Ω–¥–µ, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–≤–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
            return f"‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–∏ –∫–æ–º–∞–Ω–¥—ã `{sub_command}`: {e}"

    # --- –ü—Ä–∏–≤–∞—Ç–Ω—ã–µ –º–µ—Ç–æ–¥—ã –¥–ª—è –∫–∞–∂–¥–æ–π –ø–æ–¥–∫–æ–º–∞–Ω–¥—ã ---
    # –æ—Å—Ç–∞–≤–ª—è–µ–º –∫–∞–∫ _get_ids –∏ _unknown_command –∫–∞–∫ –æ–±—ã—á–Ω—ã–π –º–µ—Ç–æ–¥ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞, —Ç–∞–∫ –∫–∞–∫
    # –≥–ª–∞–≤–Ω–∞—è –ø—Ä–∏—á–∏–Ω–∞ - –ê—Ä—Ö–∏—Ç–µ–∫—Ç—É—Ä–Ω–æ–µ –µ–¥–∏–Ω–æ–æ–±—Ä–∞–∑–∏–µ:
    # 1. —É –Ω–∞—Å –µ—Å—Ç—å —Å–ª–æ–≤–∞—Ä—å `self.dev_commands`, –∫–æ—Ç–æ—Ä—ã–π —Å–æ–ø–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Å—Ç—Ä–æ–∫–æ–≤—ã–µ –∫–æ–º–∞–Ω–¥—ã —Å –º–µ—Ç–æ–¥–∞–º–∏-–æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞–º–∏;
    # 2. –û—Å–Ω–æ–≤–Ω–æ–π –º–µ—Ç–æ–¥ `handle` –ø–æ–ª—É—á–∞–µ—Ç –Ω—É–∂–Ω—ã–π –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –∏–∑ —ç—Ç–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è –∏ –≤—ã–∑—ã–≤–∞–µ—Ç –µ–≥–æ, –ø–µ—Ä–µ–¥–∞–≤–∞—è —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –Ω–∞–±–æ—Ä –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤: `handler(chat_id=..., user_id=..., args=...)`.
    # –ú–µ—Ç–æ–¥ `_show_help` –∏ –Ω—É–∂–¥–∞–µ—Ç—Å—è –≤ `self`, —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø –∫ `self.dev_commands`.
    # –ï—Å–ª–∏ –º—ã —Å–¥–µ–ª–∞–µ–º _get_ids –∏ _unknown_command —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–º–∏, –∞ `_show_help` –æ—Å—Ç–∞–≤–∏–º –æ–±—ã—á–Ω—ã–º –º–µ—Ç–æ–¥–æ–º, –º—ã —Å–æ–∑–¥–∞–¥–∏–º –Ω–µ–æ–¥–Ω–æ—Ä–æ–¥–Ω—É—é —Å–∏—Å—Ç–µ–º—É: –≤ —Å–ª–æ–≤–∞—Ä–µ –±—É–¥—É—Ç —Ö—Ä–∞–Ω–∏—Ç—å—Å—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Ä–∞–∑–Ω—ã—Ö —Ç–∏–ø–æ–≤.
    # –¢–µ—Ö–Ω–∏—á–µ—Å–∫–∏ —ç—Ç–æ –±—É–¥–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å, –Ω–æ —ç—Ç–æ —É—Å–ª–æ–∂–Ω–∏—Ç –ø–æ–Ω–∏–º–∞–Ω–∏–µ –∏ –ø–æ–¥–¥–µ—Ä–∂–∫—É –∫–æ–¥–∞.
    # –°–æ—Ö—Ä–∞–Ω—è—è –≤—Å–µ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ –∫–∞–∫ –º–µ—Ç–æ–¥—ã —ç–∫–∑–µ–º–ø–ª—è—Ä–∞, –º—ã –ø—Ä–∏–¥–µ—Ä–∂–∏–≤–∞–µ–º—Å—è –ø—Ä–æ—Å—Ç–æ–≥–æ –∏ –ø–æ–Ω—è—Ç–Ω–æ–≥–æ –ø—Ä–∞–≤–∏–ª–∞:
    # "–ö–∞–∂–¥–∞—è –ø–æ–¥–∫–æ–º–∞–Ω–¥–∞ –≤ **`DevStrategy`** —Ä–µ–∞–ª–∏–∑—É–µ—Ç—Å—è –∫–∞–∫ –º–µ—Ç–æ–¥ —ç—Ç–æ–≥–æ –∂–µ –∫–ª–∞—Å—Å–∞ —Å –æ–¥–∏–Ω–∞–∫–æ–≤–æ–π —Å–∏–≥–Ω–∞—Ç—É—Ä–æ–π".

    # –ö—Ä–∞—Ç–∫–∏–π –≤—ã–≤–æ–¥: –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ _get_ids –∏ _unknown_command –∫–∞–∫ –º–µ—Ç–æ–¥–æ–≤ —ç–∫–∑–µ–º–ø–ª—è—Ä–∞ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç –µ–¥–∏–Ω–æ–æ–±—Ä–∞–∑–∏–µ, —á–∏—Ç–∞–µ–º–æ—Å—Ç—å –∏ —Ü–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å –∫–ª–∞—Å—Å–∞ DevStrategy.
    # –ù–µ–∏—Å–ø–æ–ª—å–∑—É–µ–º—ã–π `self` - —ç—Ç–æ –∫—Ä–æ—à–µ—á–Ω–∞—è –∏ –ø—Ä–∏–µ–º–ª–µ–º–∞—è –ø–ª–∞—Ç–∞ –∑–∞ —ç—Ç–∏ –∞—Ä—Ö–∏—Ç–µ–∫—Ç—É—Ä–Ω—ã–µ –ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–∞.
    # –ü–æ—ç—Ç–æ–º—É –∏–∑–º–µ–Ω—è—Ç—å –∏—Ö –Ω–µ –Ω—É–∂–Ω–æ.

    def _get_ids(self, chat_id, user_id, args):
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ —á–∞—Ç–∞. –†–∞–±–æ—Ç–∞–µ—Ç –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é."""
        return f"üÜî **User ID:** `{user_id}`\n**Chat ID:** `{chat_id}`"

    def _show_help(self, **kwargs):
        """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö dev-–∫–æ–º–∞–Ω–¥."""
        available_commands = "\n".join(f"- `{cmd}`" for cmd in self.dev_commands.keys())
        return f"üõ†Ô∏è **–î–æ—Å—Ç—É–ø–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–∞:**\n{available_commands}"

    def _unknown_command(self, **kwargs):
        """–í—ã–∑—ã–≤–∞–µ—Ç—Å—è, –µ—Å–ª–∏ –ø–æ–¥–∫–æ–º–∞–Ω–¥–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞."""
        return f"‚ùì –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –ø–æ–¥–∫–æ–º–∞–Ω–¥–∞. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ `/dev help` –¥–ª—è —Å–ø—Ä–∞–≤–∫–∏."

class DevCommand(BotCommand):
    def __init__(self):
        self.strategy = DevStrategy()

    def execute(self, text, chat_id, user_id):
        return self.strategy.handle(text, chat_id, user_id)
